package com.hailian.modules.credit.usercenter.model;

public class Result {
	private int orderNum;
	private String orderDate;
	private String deadDate;
	private String clientCode;
	private String doState;
	private String orderComName;
	private String country;
	private String reportType;
	public int getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(int orderNum) {
		this.orderNum = orderNum;
	}
	public String getOrderDate() {
		return orderDate;
	}
	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}
	public String getDeadDate() {
		return deadDate;
	}
	public void setDeadDate(String deadDate) {
		this.deadDate = deadDate;
	}
	public String getClientCode() {
		return clientCode;
	}
	public void setClientCode(String clientCode) {
		this.clientCode = clientCode;
	}
	public String getDoState() {
		return doState;
	}
	public void setDoState(String doState) {
		this.doState = doState;
	}
	public String getOrderComName() {
		return orderComName;
	}
	public void setOrderComName(String orderComName) {
		this.orderComName = orderComName;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getReportType() {
		return reportType;
	}
	public void setReportType(String reportType) {
		this.reportType = reportType;
	}
	

}
